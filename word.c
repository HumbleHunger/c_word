#include<stdio.h>
int main()
{
    int p=1;
    while(p<=3){
        printf("6");
        p++;
    }
    p=1;
    printf("\n");
    do{
        printf("6");
        p++;
    }while(p<=3);
    printf("\n");
    while(0){
        printf("while\n");
    }
    do{
        printf("do_while\n");
    }while(0);
}
